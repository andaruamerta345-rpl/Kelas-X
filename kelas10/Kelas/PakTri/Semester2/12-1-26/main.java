public class Main {
    public static void main(String[] args) {
        // ====== type data int ======
        int angka;
        angka = 10;
        System.out.println(angka);
        System.out.println("angka yang anda input adalah " + angka);


        angka = 100;
        System.out.println("angka yang anda input terbaru adalah " + angka);


        // ====== type data double ======
        double angka2 = 10.5;
        System.out.println(angka2);

        angka2 = 100.5;
        System.out.println("angka yang anda input terbaru adalah " + angka2);

        // ====== type data char  ======
        char karakter = 'A';
        System.out.println(karakter);
        karakter = 'B';
        System.out.println("karakter yang anda input adalah " + karakter);

        // ====== type data boolean ======
        boolean benar = true;
        System.out.println(benar);
        benar = false;
        System.out.println("nilai boolean yang anda input adalah " + benar);

        // ===== type data String ======
        String nama = "Andaru";
        System.out.println(nama);
        nama = "Dayat";
        System.out.println("nama yang anda input adalah " + nama);

        // ====== type data final ======
        final String NAMA_SEKOLAH = "SMKN 2 Buduran Sidoarjo";
        System.out.println(NAMA_SEKOLAH);
    }
}